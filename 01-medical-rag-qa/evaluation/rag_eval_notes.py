# 案例1: AI 培训考核
    # 领导讲话 → ASR 语音识别转文字 → AI 出 10 道选择/判断题 → 现场 15 分钟闭卷作答
    # 8~10 题通过, 6~7 题补考一次, ≤5 题扣绩效
    # 应用: 企业新品发布培训、学校教育考核

# 案例2: 母婴产品 AI 销售机器人
    # 客户群体: 宝妈
    # 扫码领红包 → AI 机器人弹出 → 语音引导"用什么奶粉/纸尿裤?"
    # 核心: 营销话术设计 > 技术, 产品设计比技术更重要
    # 因为只能进行有限轮次对话, 轮次太多宝妈会烦

# 案例3: AI 旅游需求
    # 设计轻量化小产品, 贴纸大小, 贴在导游身上
    # toC: 7×24 小时无感采集导游用嗓强度 + 身体姿态, 生成个性化"健康日报"
    # toB: 旅行社"团队健康驾驶舱", 管理层实时看全队健康负荷热力图
    # 风险累积时通过震动马达和视觉提示强提醒

# 案例4: AI 女友需求
    # 模块: 普通机器人创建/列表/聊天记录 + 虚拟女友聊天/定制 + 支付订阅
    # 好感度由大模型靠提示词给出, 做好防提示词注入攻击
    # 性格难度档位 (越冷淡越难涨好感), 主题频率按付费
    # 分级月费, 解锁女友/高级模型/额度 (Stripe 订阅 plan)

# 案例5: AI 智慧屏幕
    # 音视频互动智能体: 能听/会说/能看/懂思考
    # 屏幕理解: 视觉理解 + 语音互动
    # 语气表情关联: 根据回答内容切换数字人表情
    # 记忆: 上下文管理 (短期) + 记忆库 (长期)
    # 联网搜索: Agent 接入实时互联网信息
    # 实时字幕: 语音转文字实时显示
    # 适用: AI 智能助手/客服/陪伴/教学/游戏/智能硬件

# 案例6: 养老机器人
    # 医药公司需求, 使用对象: 中老年人
    # 功能: 提醒用药 + 回答日常问题 + 顺便推销产品
    # 语音唤醒与识别, 本地技能 (闹钟/天气/音乐)
    # 15° 倾斜屏幕 + 隐私滑盖 + 热敏打印 + SOS 红色按钮连远端呼叫中心

# 案例7: 政务 AI 机器人
    # 实时交互数字人, 本地化部署
    # 用户可自定义形象和声音, 接入通用大模型和本地模型
    # 支持知识库上传, 支持中英文对话
    # 应用: 政府机关/医院/学校 → 智能问答客服和引导功能
    # 硬件: 触摸一体机, 具备收音功能

# 案例8: AI 批改作业机器人
    # 目标用户: K12 学生/教师/家长
    # 技术: 微信小程序 + DeepSeek API + 后台管理
    # Part1: 作业拍摄与上传 (高拍仪/手机, 自动裁剪旋转增强)
    # Part2: 批改流程 (手写本/练习册/试卷, ✓/× 标记, 总分统计)
    # Part3: 学情分析 (知识点错误率排行, 学习趋势图表)
    # Part4: 错题本管理 (自动收集, 含原题图片/正确答案/解析/相关知识点)

# 案例9: 医药公司 AI 宣传
    # 用户管理: 微信小程序/H5 为载体, 邀请用户参与活动
    # 后端: 建知识库 → AI 系统实现文案/脚本/文生图/视频拆解/图生视频
    # 核心: 基于知识库内容"文生视频", 用户只需提供 prompt, 自动生成大量视频
    # MCN 和达人扫码领取视频 → 抖音/小红书/快手分发 → 数据反馈给系统优化


