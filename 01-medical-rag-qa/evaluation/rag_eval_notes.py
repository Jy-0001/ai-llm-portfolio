# 核心认识
    # RAG 的评估不只是算一个分数, 而是判断系统: 稳不稳? 准不准? 能不能上线?
    # 企业项目中 RAG 的两个主要目标:
        # ① 让模型更准 (减少幻觉)
        # ② 让系统更稳 (保证一致性和可复现)
    # 大多数人只关注"生成效果好不好看", 但成熟系统必须回答三个层面:
        # ① 检索准确性：检索有没有找到对的材料?
        # ② 生成一致性：生成是否忠于这些材料?
        # ③ 系统性：整体体验是否稳定、可持续?
    # 三个层面对应 RAG 的三个核心模块: 检索 → 融合 → 生成


# 第一层: 检索评估 (Retrieval Evaluation)
    # Recall@K (召回率)
        # Recall = 回忆/召回, @ = at, K = 取前 K 条上限
        # 找到的相关文档 / 数据库中全部相关文档, 代表覆盖面 (有没有漏网之鱼)
            # e.g. 数据库共 10 篇相关文档, 取前 K=5 条中找到了 5 篇 → Recall@20 = 5/10 = 0.5
        # K 影响分子 (返回越多越可能找到更多正确的), 分母固定不变
        # 注意：如果k小于分母，则尽管召回中全部是相关的，分数也不是最高。但盲目追高 K 会引入噪声
    # Precision@K (精确率)
        # Precision = 精确/准确
        # 找到的相关文档 / 实际返回的 K 条, 代表返回结果的纯净度 (有多少是垃圾)
        # e.g. 数据库共 10 篇相关文档，取前 K=10 条, 其中 6 条真的相关 → Precision@10 = 6/10 = 0.6
        # K 就是分母, K 越大 Precision 越容易下降
        # 过低意味着结果噪声太多, 拖垮生成效果
    # MRR (Mean Reciprocal Rank, 平均倒数排名)
        # Reciprocal = 倒数, Rank = 排名
        # 衡量"第一个正确答案出现的排名"：正确答案常排在前两位 → 说明排序能力好
        # 注意：与Recall@K和Precision@K不同，MRR需要多次检索才能取平均，单词检索只是RR，而前两者单词检索也有意义
    # nDCG (Normalized Discounted Cumulative Gain, 归一化折损累积增益)
        # Cumulative=累积, Discounted=折损(位置越靠后打折越多), Gain=相关性得分, Normalized=归一化到0~1
        # 计算: Σ(相关性分数 × 位置权重) / 理想排序下的最高得分
            # 相关性分数由人类来打
        # 位置权重用 log₂ 而非倒数: 倒数衰减太激进(第1→2名掉一半), log 更贴近用户实际浏览行为
        # 理想得分(IDCG): 把所有文档按相关性从高到低排, 乘位置权重加总
        # 不仅看"找没找到"(Recall), 还看"相关的有没有排靠前"
        # 比 Recall/Precision 更严格: 找到但埋在第8位, nDCG 仍然低
    # 关键认知: 在 RAG 里, 比单纯的检索指标更重要的是把"召回正确率"和"生成可靠性"结合起来看


# 第二层: 生成一致性评估 (Generation Consistency)
    # RAG 生成阶段最怕两个问题:
        # 幻觉 (Hallucination): 模型编故事, 生成不存在的信息
        # 答非所问 (Faithfulness 缺失): 不忠于检索到的材料
    # 评估重点: 生成内容是否忠于检索材料

    # 1. 自动化指标
        # embedding相似度：Faithfulness Score (忠实度)
            # Faithfulness = 忠实/忠诚
            # 计算生成答案与检索片段在语义空间的相似度
            # e.g. 把回答和知识片段都转成 embedding, 算余弦相似度
            # 低于阈值 (如 0.65) 标记为"潜在幻觉"
        # BLEU/ROUGE：Groundedness Score (有据度)
            # Grounded = 有根据的
            # 判断模型输出的每一条结论, 是否都能在检索片段中找到依据
        # LLM评估：Factual Consistency (事实一致性)
            # 基于 LLM 自检的思路, 让另一个模型去判断答案是否自洽、是否编造

    # 2. 人工评测指标 (机器算不出来的, 需要人判断)
        # 正确性 (Correctness): 回答是否事实正确
        # 完整性 (Completeness): 答案是否覆盖了核心信息
        # 可读性 (Fluency): 语言自然, 表达流畅
        # 引用充分性 (Attribution): 是否标明信息来源
        # 适用场景: 金融、医疗、政务等对合规性要求极高的领域


# 第三层: 系统性评估 (System Evaluation)
    # 模型回答再"聪明", 速度慢/结果不稳/更新滞后, 也无法用于生产
    # 延迟 (Latency): 检索 + 生成全流程响应时间
        # 用 P50/P80/P95/P99 百分位衡量
        # P95 = 95% 的请求在 X 秒内完成
        # P99 明显高于平均值 → 检查瓶颈 (检索慢? 生成模型卡? 网络延迟?)
    # 吞吐 (Throughput/QPS): 高并发下性能稳定性
        # QPS = Queries Per Second, 每秒处理的查询数
        # 随规模增长, 响应耗时上升、吞吐量下降 → 索引结构是否高效
    # 缓存命中率 (Cache Hit Rate): 是否重复计算
    # 可复现性 (Reproducibility): 同样问题是否输出一致
    # 时效性 (Recency): 知识库更新后, 是否能实时反映到答案中
    # 在线监控指标
        # Error Rate: 生成失败或超时的比例
        # Rejection Rate: 模型拒答率 (说明数据覆盖不足)
        # Drift Monitor: 知识库更新后, 模型响应是否偏离标准
        # 用户点赞率: 终端用户的反馈数据
    # 关键认知: 企业应用场景 (智能客服/知识问答) 中, 工程指标的重要性往往高于生成效果
        # "用户等 5 秒没结果" 就已经是项目失败了


# 具体的评估实施步骤 (五步)
    # Step1: 离线验证 (Offline Eval)
        # 上线前用标注集做静态评估
        # 准备问题-答案对, 对应检索结果
        # 测 Recall@K, Faithfulness, BLEU, ROUGE
        # 目标: 发现显性问题 (检索偏差大, 分块错误多, 幻觉过多)
    # Step2: 灰度验证 (A/B Test)
        # 在部分真实流量上测新版本效果
        # 对比用户满意度或人工打分
        # 对比生成内容被"引用"的比例
        # 对比响应速度
        # A/B 测试能判断: 改进方案是否真的带来收益
    # Step3: 在线监控 (Online Eval)
        # 系统上线后仍需持续监控
        # Error Rate / Rejection Rate / Drift Monitor / 用户点赞率
        # 核心: 让 RAG 从实验室系统变成生产系统
    # Step4: 高风险行业评估 (金融/医疗/政务)
        # 重点不在"生成得多聪明", 而在:
            # 有没有编造信息
            # 回答是否可追溯
            # 引用材料是否权威, 时效性是否合规
        # 三重验证:
            # 检索层面: 必须来源于标注过的权威文档 (非网络数据)
            # 生成层面: 答案需保留引用 ID, 如 [Doc_1218]
            # 审查层面: 人工抽样检测是否"引用不一致"或"总结错误"
        # 医疗报告中一句话差之毫厘 → 误导后续医治和手术 → 评估目标不是"生成多流畅"而是"回答一定要对"
    # Step5: 用户体验
        # 用户满意度 (人工打分或反馈比例)
        # 答案可读性 (文本清晰度与可理解性)


# RAG 评估的哲学 (平衡)
    # Recall 太高 → 容易引入噪声
    # Precision 太高 → 可能容易漏掉关键信息(recall低)
    # 生成过度约束 → 会损失表达能力
    # 生成过于自由 → 则会幻觉频出
    # 成熟的工程决策不会追求单一指标最优, 而是追求"可控与可解释"
    # 合格的答案不是"指标是多少", 而是能说清楚:
        # ① 我们为什么选这些指标
        # ② 每个指标的权重, 它们之间的取舍


# RAG 评估代码示例 (BLEU + ROUGE + 覆盖率)
    # BLEU (Bilingual Evaluation Understudy, 双语评估替补)
        # 衡量生成文本与参考文本的 n-gram 重叠度
        # > 0.6 说明相似度较高
    # ROUGE (Recall-Oriented Understudy for Gisting Evaluation)
        # 面向召回的摘要评估, 衡量生成文本覆盖了多少参考文本的内容
        # > 0.7 说明覆盖面较好
    # 覆盖率: 生成答案中的 token 有多少来自检索文档
        # > 0.7 说明大部分回答来自于检索文档, 可信度较高
    # 见 eval_demo.py


# 面试标准回答模板
    # "我们制定了一个五维度的评估体系, 从检索评估、生成一致性评估、系统性评估、
    #  一些列指标全面衡量系统表现:
    #  - 检索评估通过 BLEU, ROUGE, MRR, nDCG, Top-k 召回率衡量
    #  - 生成一致性评估包含了自动化评估与人工评测
    #  - 系统性评估考差了延迟, 吞吐, 缓存命中, 可复现, 时效性等指标
    #  - 可信度通过答案-文档匹配度和覆盖率评估
    #  - 性能用平均延迟和 P95/P99 指标监控
    #  - 可扩展性通过不同数据规模的吞吐量测试验证
    #  - 用户体验则结合人工满意度评分与可读性分析
    #  通过这些维度, 我们可以系统发现瓶颈, 指导优化, 实现 RAG 系统在生产环境的稳定落地."
    # 这类回答让面试官觉得你不仅仅是在"做 RAG 项目", 你是在做 RAG 产品!!!


