# 企业需求案例的面试价值
    # 了解真实落地场景, 面试时能举出具体例子
    # 知道 AI 落地不只是技术, 还有产品设计/话术设计/合规要求
    # 不同行业的评估标准不同: 医疗要"回答一定要对", 客服要"5秒内响应", 销售要"话术设计"
