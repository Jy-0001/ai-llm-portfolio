# 大模型上线前的测试流程
    # 1. 通用能力测试 (General Capability)
        # 说明：
            # 测试模型在广泛知识领域(数学、科学、历史、法律等)的综合表现
            # 衡量"模型基础是否扎实",通常用大规模多选题数据集
            # 适用所有 LLM, 不依赖具体应用场景
        # 标准:
            # GLUE/CLUE: 对现代大模型太简单了,但是例行测试
                # GLUE = General Language Understanding Evaluation, 英文
                # CLUE = Chinese Language Understanding Evaluation, 中文
                # 覆盖: 文本分类、句对匹配、NLI、阅读理解等基础 NLP 任务
                # 已被现代 LLM "刷爆", 大多在 90+ 分, 区分度低
            # MMLU (Massive Multitask Language Understanding)
                # 57 个学科,15000+ 道 4 选 1 题, 涵盖 STEM/人文/社科
                # 评估模型的零样本/少样本知识广度
                # 现代旗舰模型大多 80%+, 也接近饱和
            # MMLU-pro (此处用的标准)
                # MMLU 升级版, 14 个分类, 10 选 1 (难度提升)
                # 题目更难、选项更多、需要推理而非记忆
                # 评估逻辑: 见 run_qwen3.py
                # 输出: 准确率 (回答 A-J 字母 vs 标准答案)
            # 其他: BIG-Bench, AGIEval, C-Eval (中文综合), HumanEval (代码)
    # 2. Domain Evaluation 垂直领域能力测试: 最重要的评估
        # 说明:
            # 评估模型在垂直项目场景(医疗/法律/金融等)下的表现
            # 通用能力强 ≠ 垂类好, 微调后必须做垂类评估
            # 没有现成基准, 一般用自己的领域验证集
        # 方法:
            # Embedding 模型评估 (此处用的标准)
                # 原理: 把"模型回答"和"标准答案 chosen"都向量化,算 cosine 相似度
                # 相似度高 = 语义接近标准答案 = 模型好
                # 优点: 抓语义不抓字面, 同义不同字也能识别
                # 项目使用 GLM Embedding 模型 (智谱 embedding-3)
                    # 为什么不用自己训的 SimCSE: SimCSE 是任务专用(医学纠错),
                    #   通用语义相似度上不如通用 embedding
                # 代码逻辑: 见 domain_test.py
                # 局限: 只评"接近度", 不评事实正确性
            # BLEU / ROUGE (字面对比)
                # 计算 n-gram 重叠率, 适合翻译/摘要等"应该字面接近"的任务
                # 垂类开放问答不太适合(同义不同字得低分)
            # LLM as judge (用更强模型当裁判)
                # 让 GPT-4/DeepSeek 给两个回答打分, 看谁更好
                # 灵活但成本高, 且裁判模型自身偏好会影响结果
            # 人工评估
                # 真人专家逐条打分, 最准但最贵最慢
                # 一般作为最终验收, 不做日常迭代
            # 多模型对比 (A/B 测试)
                # 同一题让多模型回答, 让人或 LLM 选哪个更好
                # 输出: win rate
    # 3. Instruction Following 指令跟随能力评测
        # 说明:
            # 评估模型"听不听话"——能否按指令的约束(长度/格式/语气等)输出
            # 知识对但格式不对、跑题、罗嗦, 都是指令跟随失败
            # 微调后常见问题: 模型学会知识但忘了听指令(过拟合医学知识丢通用能力)
        # 方法:
            # LLM as judge (此处用的标准)
                # 原理: 让强模型读指令 + 模型回答, 判断是否符合指令
                # Prompt 模板: "请评估以下回答是否符合指令要求, 1-10 分"
                # 代码逻辑: 见 instruction_following_test.py
                # 优点: 灵活, 能评估任何抽象指令
                # 缺点: 裁判模型有偏见(倾向喜欢长回答、礼貌语气等)
            # IFEval (规则评估)
                # 用代码硬性检查约束: "字数 ≤ 100"、"必须包含某关键词"
                # 可自动化, 客观无偏见, 但只能测可量化的约束
            # MT-Bench
                # 80 条多轮对话, GPT-4 当裁判, 评对话连贯性 + 指令跟随
            # AlpacaEval
                # 805 条指令, 对比模型回答和 text-davinci-003 的胜率
    # 4. 安全性 (Safety)
        # 说明:
            # 评估模型会不会输出有害内容(歧视、违法、自残建议等)
            # 医疗场景特别重要: 不能给危险用药建议、不能替代医生诊断
            # 中国法规要求(《生成式人工智能服务管理暂行办法》)上线前必须过
        # 方法:
            # Red Team 红队测试
                # 故意构造恶意 prompt 攻击模型, 看会不会出问题
                # 攻击类型: 越狱(jailbreak)、prompt 注入、诱导有害内容等
            # 安全基准数据集
                # SafetyBench, ToxiGen, RealToxicityPrompts
                # 中文: CValues (价值观对齐评估)
            # LLM as judge (评有害性)
                # 让裁判模型判断回答是否含有害内容
            # 拒绝率统计
                # 应该拒绝的请求(违法/危险/隐私), 模型拒绝了多少
                # 但也不能拒答过多(影响有用性)
    # 5. 效率 (Efficiency)
        # 说明:
            # 评估部署成本: 速度、显存占用、并发能力
            # 影响用户体验和服务器成本, 是上线决策的关键
        # 方法:
            # 延迟 (Latency)
                # TTFT (Time To First Token): 首字延迟, 体感关键
                # ITL (Inter-Token Latency): token 间延迟, 决定流式速度
                # 端到端延迟: 一次完整请求耗时
            # 吞吐 (Throughput)
                # tokens/s: 每秒生成多少 token
                # QPS: 每秒能处理多少请求
            # 显存占用
                # 模型权重 + KV cache + 激活值
                # 决定单卡能跑多大模型、多少并发
            # 工具
                # vLLM benchmark: 主流推理引擎自带压测
                # LLMPerf, Inferbench: 第三方对比工具
    # 评估流程总结
        # 阶段 1: 训练完先做"通用能力"测 (MMLU-Pro)
        #         → 看微调有没有把模型搞坏 (灾难性遗忘)
        # 阶段 2: 做"垂类效果"测 (embedding 相似度)
        #         → 看微调有没有真的学到领域知识
        # 阶段 3: 做"指令跟随"测 (LLM as judge)
        #         → 看模型还会不会听话
        # 阶段 4: "安全性" + "效率" 上线前最后把关
        # 任何一项不达标都不能上线, 需回炉调参或重训

# Stress Testing压力测试
    # 说明：评估在高并发场景下模型服务的承受能力
    # 指标：
        # 生成式大模型压测指标：
            # TTFT (Time To First Token --- 首字延迟时间)
                # 定义：从客户端发送完整请求(含prompt等参数)到接收到模型生成的第一个token的时间间隔，单位为毫秒(ms)或秒(s)
                # 测量方法：在客户端记录请求发送时间戳(t_send)和第一个token接收时间戳(t_first_token),计算TTFT= t_first_token - t_send, 需统计多次请求的平均值，尤其统计P95/P99分位数(避免极端值影响)
            # TPOT (Time Per Output Token --- 每令牌输出时间)
                # 定义: 生成过程中, 相邻两个 token 之间的平均时间间隔, 单位为 毫秒/token (ms/token)。
                      # 例如, 生成第 2 个 token 与第 1 个 token 间隔 50ms, 第 3 个与第 2 个间隔 60ms,
                      # 则 TPOT 为 (50+60)/2 = 55ms/token
                # 反映生成内容的"流畅性", TPOT 越高, 用户感知的"卡顿感"越强。
                # 测量方法: 在生成过程中记录每个 token 的接收时间戳 (t₁, t₂, ..., tₙ),
                          # 计算相邻时间差 (Δtᵢ = tᵢ₊₁ - tᵢ),
                          # 再求平均值或分位数 (TPOT = mean(Δt₁, Δt₂, ..., Δtₙ₋₁))

            # TPS (Token Per Second --- 每秒传输 token 数)
                # 定义: 单位时间内 (通常为秒) 模型生成的总 token 数量, 单位为 token/s,
                      # 计算方式为: TPS = (总 token 数 - 1) / (最后一个 token 时间 - 第一个 token 时间)
                    # 注意: 减 1 是因为第一个 token 的时间已包含在 TTFT 中, 因为 TPS 算的是"生成阶段的吐字速度"，第一个 token 的时间属于"启动开销"，不是生成开销。两者性质不同，混在一起会让数字失真
                # 综合反映模型的生成效率, 是衡量大模型服务"处理能力"的核心指标。
                # 对于长文本生成任务 (如文档摘要、代码生成、报告撰写), TPS 直接决定任务完成时间
                # (如生成 1000 token, TPS=100 tok/s 需 10 秒, TPS=50 tok/s 需 20 秒)。
                # 测量方法: 统计所有并发请求在单位时间内生成的总 token 数
                         # (如 10 秒内 100 个请求共生成 5000 token, 则 TPS=500 token/s),
                         # 需区分"单请求 TPS"(单个请求的生成效率) 和"集群 TPS"(整个服务的总生成能力)
        # 响应时间分布 (P50, P95, P99)
            # P95 (第95百分位数): 将所有请求的TTFT从快到慢排序后, 排在95%位置的那个值。
                                # 意味着 95% 的请求的TTFT都小于或等于这个值, 只有最慢的 5% 的请求比它差。
            # P99 (第99百分位数): 将所有请求的TTFT从快到慢排序后, 排在99%位置的那个值。
                                # 意味着 99% 的请求的TTFT都小于或等于这个值, 只有最慢的 1% 的请求比它差。
            # 为什么不用平均值: 想象你有100次请求的TTFT数据 (单位: ms)
                            # 大部分请求 (比如95次) 都很快, 在 100ms 到 200ms 之间
                            # 但有少数请求 (比如5次) 因为网络波动、服务器负载高、缓存未命中等原因,
                            #   非常慢, 达到了 1000ms, 1500ms, 甚至 2000ms
                            # 如果计算平均值, 这些极慢的请求会把平均值拉高 (例如平均可能到 250ms),
                            #   这无法真实反映大多数用户的体验, 你会误以为系统很慢,
                            #   但其实大部分用户感觉很快
            # P95 和 P99 就是为了剔除这些极端值的影响, 更科学地描述"绝大多数"请求的性能表现
        
        # QPS (Queries Per Second --- 每秒请求数)
            # 定义: 单位时间内服务成功处理的请求数量, 单位为 req/s
            # 计算方式为: QPS = 成功请求数 / 总时间 (注意: 仅统计成功请求, 排除错误请求)
            # 测量方法: 在压测工具 (如 JMeter、Locust) 中统计单位时间内成功返回的请求数
                        # (如 10 秒内成功处理 1000 请求, 则 QPS=100 req/s),
                        # 需关注"峰值QPS" (服务能承受的最大 QPS, 超过则错误率上升)
                        # 和"稳定QPS" (长期运行时保持的 QPS, 避免性能衰减)
        
        # ER (Error Rate --- 错误率)
            # 定义: 单位时间内错误请求数占总请求数的比例, 单位为百分比 (%)
            # 错误类型包括:
                # HTTP 状态码错误:
                    # 5xx (服务端错误, 如 500 Internal Server Error、502 Bad Gateway)
                    # 4xx (客户端错误, 如 400 Bad Request、413 Payload Too Large)
                # 业务逻辑错误:
                    # 模型推理失败 (如输入长度超过模型限制、输出格式不符合要求)
                    # 超时错误 (请求处理时间超过阈值, 如 30 秒)
                # 资源错误:
                    # 内存溢出 (OOM, Out of Memory)
                    # GPU 显存不足 (CUDA out of memory)
            # 直接反映服务的稳定性: 高错误率 (如超过 10%) 会导致用户体验急剧下降
                                # (如频繁报错、请求失败), 甚至影响业务可用性
                                # (如客服机器人无法回复用户)
            # 测量方法: 统计单位时间内错误请求数 (按错误类型分类),
                        # 计算 错误率 = (错误请求数 / 总请求数) × 100%
                        # 需区分"瞬时错误率" (突发流量导致) 和"持续错误率" (系统缺陷导致)
        
        # Throughput (吞吐量)
            # 定义: 单位时间内系统处理的工作量, 需结合任务类型定义:
                # 生成式任务: 通常用"每秒生成 token 数" (即 TPS) 作为吞吐量, 反映内容生成效率
                # 判别式任务 (如文本分类、命名实体识别): 用"每秒处理样本数"作为吞吐量,
                                                    # 反映分类/标注效率
                # 通用场景: 也可用"每秒处理数据量" (如 MB/s) 反映数据传输或处理能力
            # 与 QPS 互补: QPS 反映"请求数量", 吞吐量反映"实际完成的工作量"
            # 例如, 两个服务的 QPS 均为 100:
                # 服务 A 每个请求生成 10 token (吞吐量 = 1000 token/s)
                # 服务 B 每个请求生成 100 token (吞吐量 = 10000 token/s)
                # 结论: 服务 B 的实际处理能力更强
