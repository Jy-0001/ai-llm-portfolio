# 向量数据库 Faiss (Facebook AI Similarity Search)
    # 说明：相似性搜索和聚类库，专⻔⽤于处理⼤规模向量数据的搜索和检索任务, 能够快速⽐较⼤量向量, 并找出与给定向量最相似的向量或集合.
    # 核心功能：
        # 相似性搜索: 在⼤规模数据集中查找与给定向量最相似的向量.
        # 聚类: 将数据集分成若⼲个相似的簇, 每个簇内部的向量相似度较⾼.
        # 量化: 将原始的向量数据转换为更紧凑的形式, 以便在存储和传输时节省空间.
    # 应⽤场景
        # 图像检索: 构建图像特征的索引, 实现快速的相似图像搜索和图像聚类.
        # ⽂本搜索: 将⽂本转换为向量(Word2Vec, BERT), 使⽤Faiss来存储和检索这些向量, 实现⽂本相似性搜索.
        # 推荐系统: 存储⽤户的偏好向量和物品的特征向量, 快速找到与⽤户偏好相似的物品, 并推荐给⽤户.
        # ⽣物信息学: 存储和检索基因序列, 蛋⽩质结构等⽣物数据的特征向量, 帮助研究⼈员快速发现具有相似特性的⽣物分⼦.
        # 视频分析: 存储和检索视频帧的特征向量, 实现视频内容的快速检索和分析, 如⼈脸识别, ⾏为识别等.
    # 主要特点
        # ⾼效性: Faiss能够处理⼤规模数据集, 并在⾼维空间中进⾏⾼效的相似性搜索.
        # 可扩展性: Faiss⽀持多种索引类型, 可以根据数据集的规模和查询的实时性要求选择合适的索引.
        # 灵活性: Faiss提供了多种算法和数据结构, 适⽤于不同的应⽤场景和需求.
    # 索引类型
        # IndexFlatL2: ⼀种简单的暴⼒搜索索引, 直接计算查询向量与数据库中所有向量的L2距离, 以找到最相似的向量, 适⽤于⼩规模数据集.
        # IndexIVFFlat: 基于向量量化的索引结构, 适⽤于⼤规模的向量数据集, 通过将向量空间划分为多个量化区域, 并在每个区域中建⽴⼦索引, 可以显著提⾼搜索速度.
        # HNSW: ⼀种基于图结构的索引, 适⽤于需要⾼效近似最近邻搜索的场景, HNSW索引通过构建分层的NSW图来提⾼搜索效率.

